            string strConn = MetroLib.DBHelper.GetConnectionString();
            using (OleDbConnection conn = new OleDbConnection(strConn))
            {
                conn.Open();

                // TSQL문장과 Connection 객체를 지정   
                using (OleDbCommand cmd = new OleDbCommand(sql, conn))
                {
                    cmd.Parameters.Clear();
                    cmd.Parameters.Add(new OleDbParameter("@1", reqdt + "%"));

                    // 데이타는 서버에서 가져오도록 실행
                    OleDbDataReader reader = cmd.ExecuteReader();
                    while (reader.Read())
                    {
                        CData data = new CData();
                        data.Clear();
                        data.PID = reader["PID"].ToString();
                        data.PNM = reader["PNM"].ToString();
                        data.RESID = reader["RESID"].ToString();
                        data.STEDT = reader["STEDT"].ToString();
                        data.EXAMC = reader["EXAMC"].ToString();
                        data.RSLT = reader["RSLT"].ToString();

                        data.BDODT = reader["BDODT"].ToString();
                        data.QFYCD = reader["QFYCD"].ToString();
                        data.JRBY = reader["JRBY"].ToString();
                        int.TryParse(reader["UNISQ"].ToString(), out data.UNISQ);
                        int.TryParse(reader["SIMCS"].ToString(), out data.SIMCS);

                        list.Add(data);
