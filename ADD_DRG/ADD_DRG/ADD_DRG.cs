        private void Query()
        {
            List<CData> list = new List<CData>();

            grdMain.DataSource = null;
            grdMain.DataSource = list;

            string reqdt = txtReqdt.Text.ToString();

            string sql = "";
            sql = "";
            sql += System.Environment.NewLine + "SELECT A.PNM,A.RESID,A.STEDT,A.EXAMC,A.RSLT,A.BDODT,A.QFYCD,A.JRBY,A.PID,A.UNISQ,A.SIMCS ";
            sql += System.Environment.NewLine + "  FROM TI2A A WITH (NOLOCK)";
            sql += System.Environment.NewLine + " WHERE A.BDODT LIKE ? ";
            sql += System.Environment.NewLine + "   AND A.DONFG='Y'";
            if (cboQfyfg.SelectedIndex == 0)
            {
                sql += System.Environment.NewLine + "   AND LEFT(A.QFYCD,1) IN ('2','3')";
            }
            else if (cboQfyfg.SelectedIndex == 1)
            {
                sql += System.Environment.NewLine + "   AND A.QFYCD LIKE '2%'";
